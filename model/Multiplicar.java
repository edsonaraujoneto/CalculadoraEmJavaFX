package model;

public class Multiplicar {
    public int multiplicarInt(int n1, int n2) {
        return n1 * n2;
    }    

    public float multiplicarFloat (float n1, float n2) {
        return n1 * n2;
    }

    public double multiplicarDouble (double n1, double n2) {
        return n1 * n2;
    }

}
